En caso que se cierre la session, se debe cargar de nuevo el módulo y volver a iniciar session<br><br>

La pantalla está dividida en 3 secciones, la de arriba Sección de consulta<br>
La de abajo es la sección de resultados<br>
La del medio es la sección de modificación<br>

Una vez grabados los cambios en la pantalla de modificación los cambios se verán actualizados<br>
después que del formulario de consulta se vuelva a enviar la consulta.<br><br>

La lógica de funcionamiento de las secciones de la pantalla, cuando se envía la información de<br>
tipo cambio fabrica/gerencia y el detalle de los costos se envía desde la sección de consulta al la<br>
sección de resultados y de la sección de resultados a la sección de modificación.<br><br>

Por lo tanto si se cambia el tipo de modificación de precio y el detalle se debe<br> 
volver a enviar la consulta hacia la sección de resultados<br>


